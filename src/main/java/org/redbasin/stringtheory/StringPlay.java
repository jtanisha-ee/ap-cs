public class StringPlay {

  public static void main(String[] args) {

    int x = 2, y = 3;

    // this prints as a string concatenation
    System.out.println("x + y" + " = " + x + y);

    // this does actual numerical calculation
    System.out.println("x + y" + " = " + (x + y));
  }
}
